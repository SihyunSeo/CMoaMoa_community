package reply;

public class replyBean {
	int rep_no;
	String rep_name;
	String rep_content;
	String rep_write_date;
	
	public int getRep_no() {
		return rep_no;
	}
	public void setRep_no(int rep_no) {
		this.rep_no = rep_no;
	}
	public String getRep_name() {
		return rep_name;
	}
	public void setRep_name(String rep_name) {
		this.rep_name = rep_name;
	}
	public String getRep_content() {
		return rep_content;
	}
	public void setRep_content(String rep_content) {
		this.rep_content = rep_content;
	}
	public String getRep_write_date() {
		return rep_write_date;
	}
	public void setRep_write_date(String rep_write_date) {
		this.rep_write_date = rep_write_date;
	}
	
	
	
	
}
